package com.example.metrics;

import java.io.FileInputStream;
import java.io.IOException;
import java.util.Properties;

public class MetricsLoader {

    public MetricsRegistry loadFromFile(String path) throws IOException {
        Properties props = new Properties();
        try (FileInputStream fis = new FileInputStream(path)) {
            props.load(fis);
        }

        // I replaced new MetricsRegistry() with MetricsRegistry.getInstance().
        // This ensures that when we load metrics from the file 
        // we are updating the same global instance used by the rest of the application rather than creating a disconnected throwaway object.
        
        MetricsRegistry registry = MetricsRegistry.getInstance();

        for (String key : props.stringPropertyNames()) {
            String raw = props.getProperty(key, "0").trim();
            long v;
            try {
                v = Long.parseLong(raw);
            } catch (NumberFormatException e) {
                v = 0L;
            }
            registry.setCount(key, v);
        }
        return registry;
    }
}

